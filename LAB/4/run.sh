# Purpose: Demonstrate vulnerabilities in web authoring tool-generated code
# Version: 2.0

set -e

# Colors for output
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m' # No Color

# Global variables
MYSQL_ROOT_PASSWORD="root123"
